int add(const int a, const int b)
{
    return a + b;
}

int sub(const int a, const int b)
{
    return a - b;
}

int multiply(const int a, const int b)
{
    return a * b;
}

int divide(const int a, const int b)
{
    return b != 0 ? a / b : 0;
}

int modulo(const int a, const int b)
{
    return b != 0 ? a % b : 0;
}
